if __name__ == '__main__':
    # Incremental loop for printing hello for 3 times
    i = 0
    while i < 3:
        print("Hello")
        i = i + 1
    # Decremental loop for printing hello for 3 times
    i = 3
    while i > 0:
        print("Hello")
        i -= 1

    # i = i + 1 => i+=1 -> Assignment Operator
